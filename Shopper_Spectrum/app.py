import streamlit as st
import numpy as np
import joblib

# ---- Compatibility for old/new Streamlit versions ----
try:
    cache_resource = st.cache_resource      # Streamlit ≥ 1.18
except AttributeError:
    cache_resource = st.cache              # Older Streamlit fallback

# ==============================
# 1. Load Saved Models
# ==============================

#@st.cache_resource
@cache_resource
def load_product_model():
    model = joblib.load("product_similarity_model.pkl")
    return model

#@st.cache_resource
@cache_resource
def load_rfm_model():
    model = joblib.load("rfm_kmeans_segment_model.pkl")
    return model


# ==============================
# 2. Helper Functions
# ==============================

def recommend_similar_products(product_name, product_model, top_n=5, min_similarity=0.0):
    product_similarity = product_model["product_similarity"]
    product_names = product_model["product_names"]
    product_index = product_model["product_index"]

    if not product_names:
        return None, []

    query = product_name.strip().lower()

    # exact match
    exact_matches = [p for p in product_names if p.lower() == query]

    if len(exact_matches) == 0:
        # substring fallback
        contains_matches = [p for p in product_names if query in p.lower()]
        if len(contains_matches) == 0:
            return None, []
        base_product = contains_matches[0]
    else:
        base_product = exact_matches[0]

    idx = product_index[base_product]
    sim_scores = list(enumerate(product_similarity[idx]))
    sim_scores = sorted(sim_scores, key=lambda x: x[1], reverse=True)

    recommendations = []
    for i, score in sim_scores:
        if i == idx:
            continue
        if score < min_similarity:
            continue
        recommendations.append((product_names[i], score))
        if len(recommendations) >= top_n:
            break

    return base_product, recommendations


def predict_rfm_segment(recency, frequency, monetary, rfm_model):
    scaler = rfm_model["scaler"]
    kmeans = rfm_model["kmeans"]
    cluster_label_map = rfm_model.get("cluster_label_map", None)

    X = np.array([[recency, frequency, monetary]])
    X_scaled = scaler.transform(X)
    cluster_id = int(kmeans.predict(X_scaled)[0])

    if cluster_label_map is not None:
        label = cluster_label_map.get(cluster_id, f"Cluster {cluster_id}")
    else:
        label = f"Cluster {cluster_id}"

    return cluster_id, label


