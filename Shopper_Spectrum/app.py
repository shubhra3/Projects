def main():
    st.set_page_config(
        page_title="🛒 Shopper Spectrum – Segmentation & Product Recs",
        layout="wide",
    )

    # ---------- load models once ----------
    with st.spinner("Loading models..."):
        product_model = load_product_model()
        rfm_model = load_rfm_model()

    # ---------- sidebar navigation ----------
    st.sidebar.title("Navigation")
    page = st.sidebar.radio(
        "Go to",
        ["Home", "Clustering", "Recommendation"],
        index=2  # default to Recommendation like the screenshot
    )

    # ========== HOME PAGE ==========
    if page == "Home":
        st.title("🛒 Shopper Spectrum")
        st.write(
            """
            Welcome to the demo app.

            - **Clustering** page: RFM-based customer segmentation
            - **Recommendation** page: Item-based collaborative product recommender
            """
        )

    # ========== CLUSTERING PAGE ==========
    elif page == "Clustering":
        st.title("Customer Segmentation")

        st.subheader("RFM-based Customer Segmentation")
        st.write(
            "Enter Recency, Frequency and Monetary values to predict the customer segment."
        )

        col1, col2, col3 = st.columns(3)

        with col1:
            recency = st.number_input(
                "Recency (days since last purchase)",
                min_value=0,
                max_value=3650,
                value=30,
                step=1,
            )

        with col2:
            frequency = st.number_input(
                "Frequency (number of purchases)",
                min_value=1,
                max_value=1000,
                value=5,
                step=1,
            )

        with col3:
            monetary = st.number_input(
                "Monetary (total spend)",
                min_value=1.0,
                value=500.0,
                step=10.0,
                format="%.2f",
            )

        if st.button("Predict Cluster"):
            cluster_id, label = predict_rfm_segment(
                recency, frequency, monetary, rfm_model
            )

            st.success(f"Predicted Segment: **{label}** (Cluster ID: {cluster_id})")

            st.markdown("#### Segment Interpretation")
            if "High" in label:
                st.write(
                    "- **High-value, loyal shopper.** Consider exclusive offers and loyalty rewards."
                )
            elif "Regular" in label:
                st.write(
                    "- **Regular customer.** Maintain engagement with personalised emails and cross-sell."
                )
            elif "Occasional" in label:
                st.write(
                    "- **Occasional buyer.** Use targeted discounts and reminders to increase activity."
                )
            elif "Risk" in label or "At-Risk" in label:
                st.write(
                    "- **At-risk customer.** Try re-engagement campaigns and special offers."
                )
            else:
                st.write("- Generic cluster. Use RFM values to design your own strategy.")

    # ========== RECOMMENDATION PAGE ==========
    elif page == "Recommendation":
        # centre layout similar to the mockup
        left, main, right = st.columns([1, 3, 1])

        with main:
            st.title("Product Recommender")

            st.write("Enter a product name to get **5 similar products**.")

            product_name_input = st.text_input(
                "Enter Product Name", value="", placeholder="e.g. GREEN VINTAGE SPOT BEAKER"
            )

            if st.button("Recommend"):
                if not product_name_input.strip():
                    st.warning("Please enter a product name.")
                else:
                    base_product, recs = recommend_similar_products(
                        product_name_input,
                        product_model,
                        top_n=5,
                        min_similarity=0.0,
                    )

                    if base_product is None or len(recs) == 0:
                        st.error("No similar products found. Try another product name.")
                    else:
                        st.markdown(
                            f"**Base Product Used:** {base_product}"
                        )

                        st.markdown("#### Recommended Products")
                        for i, (prod, score) in enumerate(recs, start=1):
                            st.markdown(f"{i}. **{prod}**")

if __name__ == "__main__":
    main()