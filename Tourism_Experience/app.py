def recommend_collab(user_id, top_n=10):
    if user_id not in user_item.index:
        return []
    user_ratings = user_item.loc[user_id]
    rated = user_ratings[user_ratings > 0].index.tolist()
    if not rated:
        return []
    scores = item_sim_collab[rated].dot(user_ratings[rated])
    scores = scores.drop(rated, errors="ignore").sort_values(ascending=False).head(top_n)
    return list(scores.index)

def recommend_hybrid(user_id, top_n=10, alpha=0.7):
    if user_id not in user_item.index:
        return recommend_collab(user_id, top_n=top_n)

    user_ratings = user_item.loc[user_id]
    rated = user_ratings[user_ratings > 0].index.tolist()
    if not rated:
        return []

    collab_scores = item_sim_collab[rated].dot(user_ratings[rated]).drop(rated, errors="ignore")
    collab_scores = collab_scores / (collab_scores.max() + 1e-9)

    liked = user_ratings[user_ratings >= user_ratings[user_ratings > 0].mean()].index.tolist()
    content_scores = pd.Series(0.0, index=collab_scores.index)

    for it in liked[:5]:
        if it in content_sim.index:
            sims = content_sim.loc[it].reindex(content_scores.index).fillna(0)
            content_scores += sims

    if content_scores.max() > 0:
        content_scores = content_scores / content_scores.max()

    final = alpha * collab_scores + (1 - alpha) * content_scores
    return list(final.sort_values(ascending=False).head(top_n).index)

