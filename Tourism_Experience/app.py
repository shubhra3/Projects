def get_required_cols(pipe):
    """
    Get the exact feature columns used during training (from fitted pipeline).
    Works for sklearn Pipelines that were fit with pandas DataFrame.
    """
    if hasattr(pipe, "feature_names_in_"):
        return list(pipe.feature_names_in_)
    if hasattr(pipe, "named_steps") and "preprocess" in pipe.named_steps:
        pre = pipe.named_steps["preprocess"]
        if hasattr(pre, "feature_names_in_"):
            return list(pre.feature_names_in_)
    return None

@st.cache_resource
def load_models():
    reg_model = joblib.load(os.path.join(ARTIFACT_DIR, "rating_regression_model.joblib"))
    clf_model = joblib.load(os.path.join(ARTIFACT_DIR, "visitmode_classifier.joblib"))

    user_item = joblib.load(os.path.join(ARTIFACT_DIR, "user_item_matrix.joblib"))
    item_sim_collab = joblib.load(os.path.join(ARTIFACT_DIR, "item_similarity_collab.joblib"))
    content_sim = joblib.load(os.path.join(ARTIFACT_DIR, "item_similarity_content.joblib"))
    return reg_model, clf_model, user_item, item_sim_collab, content_sim

@st.cache_data
def load_data():
    if not os.path.exists(DATA_FILE):
        return None
    return pd.read_csv(DATA_FILE)

